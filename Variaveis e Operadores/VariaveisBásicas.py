# Variaveis são como armazenamos os dados em python, eles podem ser do tipo float, int, string e caracteres

# Declarando variaveis inteiras
a = 10
b = 30 
c = 20 
print(a + b + c)

# Declarando Strings
nome = "hugo"
print("Olá",  nome)

# Declarando floats
d = 9.8
print(d + a)

# criando uma variável para operações
soma = d + a
print("na variavel nova",soma)